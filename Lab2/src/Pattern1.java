public class Pattern1 {
	 public static void main(String[] args) 
	    {
	        int r=4;
	        int value = 1;
	        for (int i=1;i<=r;i++) 
	        {
	           for (int j=1;j<=i;j++) 
	            {
	               System.out.print(value+"\t");
	               value++;
	            }
	             System.out.println();
}}}
